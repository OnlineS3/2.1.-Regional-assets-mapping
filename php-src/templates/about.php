<h2>About</h2>

<p>Given the fact that regional profiling is the most prevailing RIS3 method 
    applied essentially by all regions, alongside with the fact that regions use 
    a variety of sources to map out a comprehensive ‘picture’ of regional 
    assets, <i>Regional assets mapping</i> tool is the main tool for integrating all 
    relevant sources into a web-based dashboard. This application helps RIS3 
    stakeholders to access basic information in a quick manner.</p>

<p>The logical steps of the application, based on its methodological description are:</p>

<ul class='dashed'>
    <li><i>Step 1.</i> Selection of the <strong>section, time period, regions</strong> and <strong>indicators</strong> the that will be mapped.</li>
    <li><i>Step 2.</i> Extraction of the selected data in <strong>table</strong> format.</li>
    <li><i>Step 3.</i> Selection of the appropriate <strong>visualisation</strong> mode for the data.</li>
    <li><i>Step 4.</i> Description and explanation of <strong>results</strong>.</li>
    <li><i>Step 5.</i> <strong>Report</strong> synthesis and regional profile overview.</li>
</ul>

<p>Following the application architecture described in previous steps, the 
    information flows within the application are given in <strong>Figure 1</strong>, below. 
    As it illustrated, the initial input coming from user defines the necessary 
    parameters, regarding the region that needs to be described and the time 
    period to which it refers to. By combining this information with the 
    selected more relevant indicators, the corresponding data is pulled from the 
    main database. The processing of these data, in terms of visualization, 
    is based on table or graphic format, which can be sent to the final report. 
    Individual tables or graphics can also be extracted through the application.
</p>

<p>The main output of the application is the final report of Regional assets 
    mapping exercise, which will give the user a comprehensive picture regarding 
    his/her region for the chosen time period frame. Moreover, individual 
    information that might be needed to some other phases of the RIS3 
    development can also be extracted through this application. Finally, it 
    should be pointed out that this application uses data coming from Eurostat.
</p>

