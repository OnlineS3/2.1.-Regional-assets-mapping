    <p class='steps'>
        Step 3 -  Selection of the appropriate visualisation mode for the data
    </p> 
    <p>The third step of the application offers the opportunity to select the 
        appropriate visualisation mode for the data by pressing the Show Graph 
        button (Fig. 3a). The choice must be done between two different 
        visualisation modes: line graphs and bar charts. In both cases, it is 
        possible to export the produced graphs in an image format (.png) by 
        pressing the Export button at the bottom of the graph. </p>
    
    
