    <p>It is important to notice that the application gives to users the 
        opportunity to group their results either based on regions, or based on 
        variables. Thus, we can obtain results in two forms: i) results for a 
        specific variable for a number of regions; and ii) results for a number 
        of different variables for a specific region. These two different option 
        provide the opportunity to perform comparative analysis not only within 
        a region, but also between different EU regions. </p>
